        <h2 class="mb-4">Sobre a disciplina</h2>

        <p>
            A disciplina GSI019 - Programação para a Internet, ministrada na Universidade Federal de Uberlândia (UFU) pela Faculdade de Computação (FACOM), sob a orientação do Professor <a href="https://www.facom.ufu.br/~rafaelaraujo/" target="_blank">Rafael Araujo</a>, é um curso que aborda conceitos essenciais na área de desenvolvimento web.
            <br><br>
            O curso foca em fundamentos sólidos de programação web, incluindo HTML, CSS e JavaScript, que são as bases para a criação de páginas web interativas, responsivas e personalizadas. Além disso, os alunos têm a oportunidade de explorar conceitos relacionados a servidores web, persistência de dados e o desenvolvimento de páginas dinâmicas por meio da linguagem de programação PHP.
            <br><br>
            Ao longo do curso, os alunos adquirem conhecimentos práticos e teóricos que são fundamentais para o desenvolvimento de aplicações web. Eles aprendem a criar interfaces atraentes, aprimorar a experiência do usuário e desenvolver páginas web que se destacam no ambiente online
        </p>

