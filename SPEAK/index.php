            <h2 class="mb-4">Sobre o SPEAK</h2>
    
            <p>
                O SPEAK é uma plataforma educacional desenvolvida como parte do projeto da disciplina Programação para Internet(GSI019) da Universidade Federal de Uberlândia. Sua criação tem como foco a abordagem do <a href="https://brasil.un.org/pt-br/sdgs" target="_blank">Objetivo de Desenvolvimento Sustentável (ODS) 4 das Nações Unidas - Educação de Qualidade.</a>
            </p>
            
            <p>
                Nossa aplicação foi desenvolvida para atender a dois públicos-chave: professores e estudantes. Ela oferece uma plataforma de ensino acessível e colaborativa, onde os professores utilizam um sistema de chat exclusivo para interagir com os estudantes e conduzir suas aulas.
                <br><br>
                Para os professores, a aplicação oferece a oportunidade de compartilhar conhecimento de maneira direta e eficaz, utilizando o chat como canal de comunicação. Isso permite que os professores forneçam instruções e informações.
                <br><br>
                Do lado dos estudantes, a aplicação oferece acesso a uma ampla variedade de cursos em diversas áreas de conhecimento. Os estudantes podem explorar a lista de cursos disponíveis, se inscrever naqueles de seu interesse e participar de sessões de chat conduzidas pelos professores.
                <br><br>
                Uma característica notável da nossa aplicação é o compromisso com a acessibilidade. Todos os cursos são oferecidos gratuitamente, tornando a educação de qualidade acessível a todos. Isso elimina barreiras financeiras e abre oportunidades educacionais para um público mais amplo.
                <br><br>
                Em resumo, nossa aplicação promove a aprendizagem colaborativa, conectando professores dedicados a estudantes ávidos por conhecimento. O chat é a ferramenta central que permite aos professores lecionar e aos estudantes adquirir conhecimento. A plataforma visa eliminar barreiras geográficas e financeiras, proporcionando uma educação inclusiva e equitativa. E para um ótimo funcionamento da nossa plataforma contamos com a <strong>colaboração essencial dos professores.</strong></p>
    
            <h3 class="mt-4">Contribuição para o ODS 4:</h3>
            <ol>
                <li><strong>Acesso Universal à Educação:</strong> O SPEAK visa eliminar barreiras geográficas e financeiras, permitindo que estudantes de baixa renda tenham acesso a cursos ministrados por professores voluntários qualificados, independentemente de sua localização.</li>
                <li><strong>Inclusão e Igualdade de Acesso:</strong> A plataforma reduz as desigualdades educacionais, proporcionando a todos os alunos, independentemente da origem socioeconômica, a oportunidade de aprender com tutores dedicados.</li>
                <li><strong>Qualidade de Ensino:</strong> Nossos professores voluntários trazem experiência ao ensino, oferecendo cursos que atendem aos padrões educacionais básicos, garantindo uma educação de qualidade para todos.</li>
            </ol>
    
